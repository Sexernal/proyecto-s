                <div class="eventos">
                    <div class="evento">
                        <div class="fecha">2000-2003</div>
                        <div class="descripcion">
                            <h4>Una epoca dificil</h4>
                            <p>En los últimos años del siglo XX y en los primeros del siglo XXI, el equipo entró en grave crisis económica,
                                el pasivo se agigantó, se atrasaba el pago a los jugadores aunque paradójicamente se realizaron inversiones
                                como la compra de la Casa Club y el fallido desarrollo de la Ciudad Deportiva, sin contenido económico,
                                proyectos que agravaron la situación del equipo. A nivel deportivo el club también estuvo en crisis, además
                                de una inestabilidad en el cuerpo técnico (tuvo nueve directores técnicos del año 2000 a inicios de 2003)
                                y durante estos años los resultados no fueron los deseados.
                            </p>
                        </div>
                    </div>
                    <div class="evento">
                        <div class="fecha">2003</div>
                        <div class="descripcion">
                            <h4>Finales de la crisis</h4>
                            <p>En un esfuerzo por salvar a la institución, la asociación se transformó en sociedad anónima el 15 de julio de 2000,
                                pero no pudo superar la crisis, entrega de desaciertos de la organización. El Estadio fue rematado por el Juzgado
                                Civil y de Hacienda de Asuntos Sumarios, pero merced a defensas legales oportunamente presentadas, el Banco
                                Popular, rematario, no pudo entrar en posesión del bien. Además de la deuda con el Banco Popular, el club mantenía
                                otra con la Caja Costarricense del Seguro Social (CCSS) y el Instituto Mixto de Ayuda Social (IMAS), este último
                                ente solicita la apertura de un proceso de quiebra contra el Saprissa. Mínor Vargas presidente el club fue
                                destituido por diversas irregularidades denunciadas por el fiscal de la junta directiva, Fabio Carballo. Tras
                                la destitución de Vargas, se designó como presidente a Fernando Villalobos; quién llevó las negociaciones con los
                                representantes de Jorge Vergara, que en forma providencial en el año 2003, adquiere la mayoría del paquete
                                accionario y logra salvar de una potencial quiebra económica al conjunto morado.
                            </p>
                        </div>
                    </div>
                </div>
