            <div class="decada">
                <h3 class="decada-titulo">Consolidación y éxitos nacionales e internacionales (1960-1999)</h3>
                <div class="eventos">
                    <div class="evento">
                        <div class="fecha">60s 70s</div>
                        <div class="descripcion">
                            <h4>2 décadas doradas</h4>
                            <p>A partir de los años sesenta y setenta el equipo comenzó una de las etapas más exitosas de su historia.
                                En la década de los sesenta el equipo ganó seis campeonatos de liga: 1962, 1964, 1965, 1967, 1968 y 1969;
                                tres copas nacionales: Copa Presidente 1960 y 1963, y Copa Campeón de Campeones 1963. En la década de los
                                setenta fue campeón de liga en seis ocasiones consecutivas de 1972 a 1977 (récord de títulos consecutivos
                                a nivel nacional), además obtuvo tres copas nacionales: Copa Costa Rica 1970, Copa Juan Santamaria
                                1972 y Copa Campeón de Campeones 1976. A nivel internacional logró ser tres veces campeón del torneo
                                Fraternidad Centroamericana 1972, 1973 y 1978.
                            </p>
                        </div>
                    </div>
                    <div class="evento">
                        <div class="fecha">80s 90s</div>
                        <div class="descripcion">
                            <h4>Una década de éxito moderado seguido de convertirse en el mejor de CR</h4>
                            <p>Durante los años ochenta el equipo vivió una etapa de cambio, Ricardo Saprissa se alejó de la junta directiva
                                luego de una gestión de 32 años como presidente, y a nivel deportivo los éxitos fueron menos en comparación
                                con los años anteriores. En los primeros años de esta década solo fue campeón nacional en 1982 y a nivel
                                internacional se proclamó campeón de la Copa Camel en 1985. Saprissa vuelve a retomar el protagonismo a
                                finales de los años ochenta obtuvo el bicampeonato en 1988 y 1989; y para la década de los noventa ganaron
                                siete títulos (internacionales y nacionales): ganó la Copa de Campeones de Concacaf en dos ocasiones en 1993
                                y 1995, uno del torneo Grandes de Centroamérica en 1998 y cuatro campeonatos nacionales en las temporadas
                                1993-94, 1994-95, 1997-98 y 1998-99. En los dos últimos campeonatos de la década marcó más de 100 anotaciones
                                (101 goles en la campaña 1997-98 y estableció el récord de más anotaciones en una sola temporada con 108 goles
                                en el torneo 1998-99). Por los logros anteriores fue declarado el Equipo del Siglo XX en Costa Rica por la
                                prensa nacional. A la vez, en un análisis de estadígrafos del fútbol costarricense designaron al Deportivo
                                Saprissa como el mejor equipo de Costa Rica y centroamericano a nivel internacional.
                            </p>
