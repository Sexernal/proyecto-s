                                un torneo de la Copa de Campeones de la Concacaf en 2005, dos subcampeonatos en las ediciones 2004 y 2008; un
                                torneo de Copa Interclubes de la UNCAF en 2003, dos subcampeonatos en las ediciones 2004 y 2007 y por último un
                                tercer lugar en la Copa Mundial de Clubes de la FIFA en 2005. Siendo esta una época dorada para el cuadro morado.
