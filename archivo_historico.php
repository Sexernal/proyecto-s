                                Durante la gestión de Jorge Vergara el Deportivo Saprissa obtuvo grandes y destacados logros tanto a nivel
                                nacional como internacional, siendo este nombrado el Equipo del siglo XX de la Concacaf por encima de grandes
                                equipos de México, Estados Unidos, Honduras, Guatemala y demás. Durante esta época el Deportivo Saprissa logró
