                            <p>En el año 2005 el Deportivo Saprissa afrontó una nueva edición de la Copa de Campeones de la Concacaf del mes
                                marzo hasta el mes de mayo de 2005, en donde se enfrentó en cuartos de final al Kansas City Wizards
                                imponiéndose sobre este con global de 2-1; posteriormente en el mes de abril jugó en semifinales contra el
                                club de fútbol Monterrey, en el primer partido celebrado en el Estadio Ricardo Saprissa el cuadro morado
                                empató con marcador de 2-2 con un gol agónico de Allan Alemán, ya en el partido de vuelta se disputó uno de
                                los partidos más duros y difíciles que haya afrontado el Saprissa durante este torneo, dado que al inicio del
                                partido expulsaron a Walter Centeno dejando al Saprissa con 10 jugadores, aunque esto pudo haber sido el fin
                                para Saprissa en el torneo, el cuadro morado impuso sus condiciones y con un jugador menos empató un partido
                                que se creía perdido para el cuadro morado, con gol de Rónald Gómez, esto hizo que el partido se fuese a un
                                decisiva tanda de penales en donde el cuadro morado se triunfó con un marcador de 5-3, haciendo que Saprissa
                                disputase una nueva final de Copa de Campeones de la Concacaf, siendo esta la segunda consecutiva, y en la
                                final efectuada en el mes de mayo el cuadro morado se enfrentó al cuadro de Pumas de la UNAM. Saprissa abrió
                                la serie en casa imponiéndose al club mexicano por marcador de 2-0 gracias a los goles de Christian Bolaños y
                                Gabriel Badilla, en el partido de vuelta disputado en el estadio Olímpico Universitario el cuadro morado
                                disputó un partido muy reñido, al final el cuadro mexicano ganó con un marcador de 2-1, el conjunto saprissista
                                ganó la final con global de 3-2 consagrándose así por tercera vez en su historia como campeón de la Copa de
                                Campeones de la Concacaf
                            </p>
