                    Igualmente, José Francisco "Chico" Hernández es recordado por su liderazgo tanto dentro como fuera de la cancha, consolidándose
                    como una pieza esencial en el engranaje saprissista.

                    Otro nombre inolvidable es el de Alonso "El Mariachi" Solís, famoso por su magia con el balón, capaz de decidir partidos
                    importantes con su talento. A él se suma Ronald "La Bala" Gómez, célebre por sus potentes remates, y
                    Gabriel "El Gladiador" Badilla, cuyo carácter y liderazgo fueron fundamentales en la historia del glorioso "Monstruo Morado".

                    Estos, junto a muchas otras leyendas que han vestido la camiseta morada, forman parte del legado que ha hecho del Saprissa
