                    Roberto Fernández quien sugirió llamarlo Deportivo Saprissa, y con el respaldo del señor Leiva, le
                    hicieron llegar una carta a don Ricardo. Este, con entusiasmo, prometió su apoyo, sellando así el
                    nacimiento oficial del Saprissa F.C. el 16 de julio de 1935.
